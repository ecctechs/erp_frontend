    // ฟังก์ชันแปลงข้อมูล JSON เป็น CSV
    convertToCSV(arr) {
      const header = Object.keys(arr[0]);
      const rows = arr.map((row) =>
        header
          .map((fieldName) =>
            JSON.stringify(row[fieldName], (key, value) =>
              value === null ? "" : value
            )
          )
          .join(",")
      );
      return [header.join(","), ...rows].join("\r\n");
    },

